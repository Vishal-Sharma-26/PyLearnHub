# Get the characters from position 2 to position 6 (not included):
a = "Python Programming"
print(a[2:9])

# Get the characters from the start to position 5 (not included):
b = "Python Programming"
print(b[:5])


# ------------------ Slice To the End --------------------

# Get the characters from position 1, and all the way to the end:
b = "Python Programming"
print(b[1:])


# ---------------------- Negative Indexing ------------------------

'''
Get the characters:
From: "m" in "Programming" (position -5)
To, but not included: "n" in "Programming" (position -2):
'''
b = "Python Programming"
print(b[-5:-2])