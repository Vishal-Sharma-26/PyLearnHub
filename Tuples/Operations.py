# 1. Concatenation (+):

t1 = (1, 2)
t2 = (3, 4)
print(t1 + t2)


# 2. Repetition (*):

print(t1 * 2)


# 3. Membership Testing (in, not in):

print(2 in t1)
print(5 not in t1)


# 4. Length (len()):

my_tuple = (10, 20, 30, 40)
print(len(my_tuple))


# 5. Iteration:

for item in my_tuple:
    print(item)