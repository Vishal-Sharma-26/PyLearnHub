'''
There are three types of numbers
1. int
2. float
3. complex
'''

# 1. int

x = 1
y = 35656
z = -3255

print(type(x))
print(type(y))
print(type(z))

# 2. float

x1 = 1.10
y1 = 1.0
z1 = -35.59

print(type(x1))
print(type(y1))
print(type(z1))

x2 = 35e3
y2 = 12E4
z2 = -87.7e100

print(type(x2))
print(type(y2))
print(type(z2))

# 3. complex

x3 = 3+5j
y3 = 5j
z3 = -5j

print(type(x3))
print(type(y3))
print(type(z3))